/**
 * First code written in Scala \o/
 * This file contains an object declaration with a main method that prints a simple Hello Scala World!.
 *
 * Take into account how the language use the "object" word to create a singleton object. In scala the usage of
 * statics is not allowed. Inside the object a main function with an Array[String] is declared using the "def"
 * word.
 *
 * @author Pedro Vicente Gómez Sánchez.
 */
object HelloWorld {
  def main(args: Array[String]) {
    println("Hello Scala World!")
  }
}
